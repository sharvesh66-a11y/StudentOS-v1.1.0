// AI Recommendations (Sprint 4.4 — Phase 2)
// ---------------------------------------------------------------------------

/**
 * Personalized recommendations generated by Junova AI based on the student's
 * memory. Single document per user at `junova_recommendations/{uid}`.
 */
export interface AIRecommendations {
  /** Document ID = user UID. */
  uid: string;

  // --- Study recommendations ---
  /** Next chapter/topic the student should study. */
  nextChapter: string | null;
  /** Topics that need revision (spaced repetition). */
  revisionTopics: string[];
  /** Practice recommendations (exercises, quizzes, past papers). */
  practiceRecommendations: string[];
  /** Daily learning goals (2–5 actionable items). */
  dailyGoals: string[];
  /** Personalized study path — ordered list of topics. */
  studyPath: string[];

  // --- Analytics ---
  /** 0–100 exam readiness score. */
  examReadinessScore: number;
  /** Human-readable explanation of the readiness score. */
  examReadinessExplanation: string | null;

  // --- Personalization ---
  /** Recommended AI Teacher ID based on learning style + subjects. */
  recommendedTeacherId: string | null;
  /** Why this teacher is recommended. */
  recommendedTeacherReason: string | null;
  /** Daily motivational insight (1–2 sentences). */
  motivationalInsight: string | null;

  // --- Timestamps ---
  createdAt: number;
  updatedAt: number;
  /** When recommendations were last regenerated. */
  generatedAt: number;
}

/** Default recommendations for a new user. */
export const DEFAULT_RECOMMENDATIONS: Omit<AIRecommendations, 'uid'> = {
  nextChapter: null,
  revisionTopics: [],
  practiceRecommendations: [],
  dailyGoals: [],
  studyPath: [],
  examReadinessScore: 0,
  examReadinessExplanation: null,
  recommendedTeacherId: null,
  recommendedTeacherReason: null,
  motivationalInsight: null,
  createdAt: 0,
  updatedAt: 0,
  generatedAt: 0,
};

/** Result of a recommendation generation operation. */
export interface RecommendationResult {
  success: boolean;
  data?: AIRecommendations;
  error?: { code: string; message: string };
}

