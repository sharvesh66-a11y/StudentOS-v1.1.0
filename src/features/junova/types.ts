// Teacher DNA (Sprint 4.2)
// ---------------------------------------------------------------------------

/**
 * Teacher DNA — 11 personality traits that modulate AI responses.
 * Each trait is a number 0–100 (0 = low, 100 = high).
 */
export interface TeacherDNA {
  /** How warm and approachable the teacher is. */
  friendliness: number;
  /** How demanding the teacher is about correctness and rigor. */
  strictness: number;
  /** How often the teacher uses humor to lighten the mood. */
  humor: number;
  /** How detailed explanations are (brief vs thorough). */
  explanationDepth: number;
  /** How patient the teacher is with repeated questions. */
  patience: number;
  /** How much the teacher encourages and motivates the student. */
  motivation: number;
  /** How often the teacher uses emojis in responses. */
  emojiUsage: number;
  /** How often the teacher uses stories / analogies. */
  storytelling: number;
  /** How often the teacher connects concepts to real-life examples. */
  realLifeExamples: number;
  /** When true, the teacher prioritizes exam-style questions and marks. */
  examFocused: boolean;
  /** Difficulty level the teacher targets. */
  difficulty: 'beginner' | 'intermediate' | 'advanced';
}

/** Default DNA for a new teacher. */
export const DEFAULT_DNA: TeacherDNA = {
  friendliness: 70,
  strictness: 50,
  humor: 40,
  explanationDepth: 60,
  patience: 80,
  motivation: 70,
  emojiUsage: 20,
  storytelling: 50,
  realLifeExamples: 60,
  examFocused: false,
  difficulty: 'intermediate',
};

// ---------------------------------------------------------------------------
// AI Teacher (Sprint 4.1)
// ---------------------------------------------------------------------------

/** Personality preset — a starting DNA template. */
export type PersonalityPreset =
  | 'friendly-mentor'
  | 'strict-professor'
  | 'study-buddy'
  | 'exam-coach'
  | 'creative-guide'
  | 'custom';

/** Teaching style — modulates how the teacher delivers content. */
export type TeachingStyle = 'socratic' | 'lecture' | 'hands-on' | 'visual' | 'story-based';

/**
 * AI Teacher — a configurable AI persona that students chat with.
 * Stored in Firestore under `junova_teachers/{teacherId}`.
 */
export interface AITeacher {
  id: string;
  /** Owner UID (Firebase Auth). */
  uid: string;
  name: string;
  /** Avatar image URL (Firebase Storage path or null for initials). */
  avatarURL: string | null;
  /** Subject this teacher specializes in. */
  subject: string;
  /** Personality preset used to initialize DNA. */
  preset: PersonalityPreset;
  /** How this teacher delivers content. */
  teachingStyle: TeachingStyle;
  /** Short biography / description. */
  bio: string;
  /** Theme color — hex string used for UI accents. */
  themeColor: string;
  /** The personality configuration. */
  dna: TeacherDNA;
  createdAt: number;
  updatedAt: number;
}

// ---------------------------------------------------------------------------
// Conversations & Messages (Sprint 4.3)
// ---------------------------------------------------------------------------

/** A chat conversation between a student and an AI teacher. */
export interface Conversation {
  id: string;
  uid: string;
  /** The AI teacher this conversation is with. */
  teacherId: string;
  title: string;
  /** Preview text for the sidebar (last message snippet). */
  lastMessagePreview: string;
  messageCount: number;
  /** Pinned conversations appear at the top of the sidebar. */
  pinned: boolean;
  createdAt: number;
  updatedAt: number;
}

/** Role of a message sender. */
export type MessageRole = 'user' | 'assistant';

/** A single message in a conversation. */
export interface Message {
  id: string;
  conversationId: string;
  role: MessageRole;
  content: string;
  /** File attachments (images, PDFs) — Firebase Storage URLs. */
  attachments?: MessageAttachment[];
  /** When true, the message is being streamed (not yet complete). */
  isStreaming?: boolean;
  /** When true, the response can be regenerated. */
  canRegenerate?: boolean;
  /** Suggested follow-up questions (assistant messages only). */
  suggestions?: string[];
  /** Review engine metadata (assistant messages only — set after review). */
  review?: ChatStreamChunk['review'];
  createdAt: number;
}

/** File attachment on a message. */
export interface MessageAttachment {
  type: 'image' | 'pdf';
  url: string;
  filename: string;
  /** For images: a text description generated by VLM (future). */
  description?: string;
}

// ---------------------------------------------------------------------------
// Chat request / response (API layer)
// ---------------------------------------------------------------------------

/** Request body for the streaming chat API. */
export interface ChatRequest {
  conversationId: string;
  teacherId: string;
  /** Full teacher config (sent from client since it was already fetched). */
  teacher: AITeacher;
  message: string;
  /** Previous messages for context (last N messages). */
  history: { role: MessageRole; content: string }[];
  /** File attachments to include in the message. */
  attachments?: MessageAttachment[];
  /** Student memory for personalization (Phase 2). */
  memory?: StudentMemory | null;
}

/** A chunk in the streaming response. */
export interface ChatStreamChunk {
  type:
    | 'delta'
    | 'done'
    | 'error'
    | 'suggestions'
    | 'review-correction'
    | 'review-enhanced'
    | 'review-meta';
  content?: string;
  suggestions?: string[];
  error?: string;
  /** Review engine metadata (present when type starts with 'review-'). */
  review?: {
    verdict: 'approved' | 'approved-with-enhancements' | 'rewritten' | 'rejected';
    scores: {
      overallQuality: number;
      accuracy: number;
      safety: number;
      childFriendly: number;
      boardExam: number;
      confidence: number;
    };
    wasRewritten?: boolean;
    issues?: Array<{
      agent: string;
      severity: 'info' | 'warning' | 'error' | 'critical';
      description: string;
      suggestedFix?: string;
    }>;
  };
}

