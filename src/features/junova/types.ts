/** Avatar visual style. */
export type AvatarStyle = 'minimal' | 'geometric' | 'friendly';

/** Avatar facial expression — drives the expression animation. */
export type AvatarExpression =
  'neutral' | 'happy' | 'focused' | 'thinking' | 'encouraging' | 'surprised' | 'speaking';

/**
 * Live session settings — single document per user at
 * `junova_live_sessions/{uid}`. Controls avatar appearance + behavior.
 */
export interface LiveSessionSettings {
  uid: string;
  /** Avatar visual style. */
  avatarStyle: AvatarStyle;
  /** How expressive the avatar is (0–100). */
  expressionIntensity: number;
  /** Animation speed multiplier (0.5–2.0, default 1.0). */
  animationSpeed: number;
  /** Whether the live teacher is in fullscreen mode. */
  fullscreenMode: boolean;
  /** Whether the whiteboard is visible. */
  whiteboardEnabled: boolean;
  /** Whether eye-contact tracking is enabled. */
  eyeContactEnabled: boolean;
  /** Whether hand gestures are enabled. */
  gesturesEnabled: boolean;
  createdAt: number;
  updatedAt: number;
}

