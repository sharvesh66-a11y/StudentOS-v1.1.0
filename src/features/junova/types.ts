  pitch: number;
  /** Speech volume (0–1.0, default 1.0). */
  volume: number;
  /** Language code for STT + TTS (e.g. "en-US", "es-ES"). */
  language: string;
  /** Automatically speak AI responses. */
  autoSpeak: boolean;
  createdAt: number;
  updatedAt: number;
